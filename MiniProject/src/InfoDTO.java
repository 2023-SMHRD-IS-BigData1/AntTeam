

public class InfoDTO {
	
	protected String id;
	protected String pw;
	protected String nick;
	
	public InfoDTO(String id, String pw, String nick) {
		this.id = id;
		this.pw = pw;
		this.nick = nick;
	}

	public String getId() {
		return id;
	}

	public String getPw() {
		return pw;
	}
	
	public String getNick() {
		return nick;
	}
	
	
	
	
	
	
	

}
